def get_four_T_S_states_arrows():
    one_states = ['0', '↓', '↑', '↑↓']
    two_states = list(itertools.product(one_states, repeat=2))
    two_states = [list(arr) for arr in two_states]
    two_states[6] = ['S']
    two_states[9] = ['T_0']
    four_states = list(itertools.product(two_states, repeat=2))
    ret_states = [state_1 + state_2 for state_1, state_2 in four_states]

    return ret_states